var ctx = document.all.lifeGrid.getContext("2d");
ctx.lineWidth = 1;
ctx.strokeStyle = "#cecece";
ctx.font = "20px Arial";

var interval,
    flag = false,
    timeout = 50,
    grid = 50,
    sizes = {
        w: 20,
        h: 20
    }

var cell = sizes.w;

var lifeGrid = new Array(grid).fill(0).map(x => new Array(grid).fill(0));

document.all.btn.addEventListener("click", e => flag = !flag);
document.all.lifeGrid.addEventListener("click", function (e) {
    let coords = getMousePos(this, e);
    let x = Math.floor(coords.x / sizes.w);
    let y = Math.floor(coords.y / sizes.h)
    lifeGrid[x][y] = 1;
    drawGrid();
});

function getMousePos(canvas, evt) {
    var rect = canvas.getBoundingClientRect();
    return { x: evt.clientX - rect.left, y: evt.clientY - rect.top };
}

function clear(ctx) { return ctx.clearRect(0, 0, ctx.canvas.width, ctx.canvas.height); }
function rand() { return Math.round(Math.random() * (1 - 0) + 0); }

function setupGrid() {
    for (let i = 0; i < lifeGrid.length; i++) {
        for (let j = 0; j < lifeGrid[i].length; j++) {
            lifeGrid[i][j] = rand();
        }
    }
}

function drawGrid() {
    clear(ctx);

    for (let i = 0; i < lifeGrid.length; i++) {
        for (let j = 0; j < lifeGrid[i].length; j++) {
            ctx.strokeRect(cell * i, cell * j, sizes.w, sizes.h);
            if (lifeGrid[i][j] > 0) {
                ctx.fillStyle = "black";
                ctx.fillRect(cell * i, cell * j, sizes.w, sizes.h);
            }
        }
    }
}

function getState(i, j) {
    let state = 0 - lifeGrid[i][j];
    for (let x = -1; x <= 1; x++) {
        for (let y = -1; y <= 1; y++) {
            state += x + i >= 0 && y + j >= 0 && x + i < lifeGrid.length && y + j < lifeGrid[0].length ? lifeGrid[x + i][y + j] : 0;
        }
    }
    return state;
}

function mutate() {
    for (let i = 0; i < lifeGrid.length; i++) {
        for (let j = 0; j < lifeGrid[i].length; j++) {
            let val = 0;
            let cell = lifeGrid[i][j];
            let state = getState(i, j);

            if (cell === 1 && (state < 2 || state > 3)) { val = 0; }
            else if (cell === 0 && state === 3) { val = 1; }
            else { val = cell }

            lifeGrid[i][j] = val;
        }
    }
    return drawGrid();
}

function playLife() {
    flag && mutate();
    start();
}

function start() {
    interval = setTimeout(() => {
        playLife();
    }, timeout);
}

setupGrid();

lifeGrid = knownGrids;
drawGrid();

start();
